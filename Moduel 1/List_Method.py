a=[10,20,30]                    #list
print(a.append(40))                     #add element at the end
print(a)
print(a.extend([50]))               #add element at the end
print(a)
print(a.insert(2,40))      #add element at the specified index
print(a)
print(a.remove(20) )      #remove specified element
print(a)
print(a.pop())         #remove last element
print(a)
print(a.pop(2))   #remove element at specified index
print(a)

print(a.count(10))  #count occurrences of an element
print(a)
print(a.index(40)) #find index of an element
print(a)
print(a.sort())  #sort the list
print(a)
print(a.reverse())#reverse the list
print(a)
print(a.clear() )  #remove all elements
print(a)
